        {
            edtSucheModul.Properties.DataSource = DBUtil.OpenSQL(@"
                    SELECT Code = ModulID,
                           Text = Name + ISNULL(' (' + ShortName + ')', '')
                    FROM dbo.XModul WITH (READUNCOMMITTED)
                    WHERE ModulID IN (-1,-7,0) OR ModulID > 0
                    UNION ALL
                    SELECT Code = NULL,
                           Text = ''
                    ORDER BY Text");
