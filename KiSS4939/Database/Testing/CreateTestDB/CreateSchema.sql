SET NOCOUNT ON;
GO

-------------------------------------------------------------------------------
-- Create Schema on Database
-------------------------------------------------------------------------------
IF(NOT EXISTS ( SELECT TOP 1 1
                FROM INFORMATION_SCHEMA.SCHEMATA
                WHERE SCHEMA_NAME = 'log'))
BEGIN
  EXEC('CREATE SCHEMA [log] AUTHORIZATION [kiss3];');
  PRINT('Info: Created new Schema "log."')
END
ELSE
  PRINT('Info: Schema "log" already exists on database.');
GO

-------------------------------------------------------------------------------
-- post-steps
-------------------------------------------------------------------------------
SET NOCOUNT OFF;
GO
