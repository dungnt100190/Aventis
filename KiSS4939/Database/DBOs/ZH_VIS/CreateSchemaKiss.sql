                FROM INFORMATION_SCHEMA.SCHEMATA
                WHERE SCHEMA_NAME = 'kiss'))
BEGIN
  EXEC('CREATE SCHEMA [kiss];');
  PRINT('Info: Created new Schema "kiss."')
END
ELSE
  PRINT('Info: Schema "kiss" already exists on database.');
GO

