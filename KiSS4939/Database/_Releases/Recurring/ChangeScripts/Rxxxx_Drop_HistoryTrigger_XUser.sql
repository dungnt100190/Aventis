  Description
-------------------------------------------------------------------------------------------------
  SUMMARY: Use this script to drop the history trigger for XUser.
=================================================================================================*/

IF EXISTS (SELECT TOP 1 1 FROM sys.triggers WHERE object_id = OBJECT_ID(N'[dbo].[trHist_XUser]'))
BEGIN
  DROP TRIGGER dbo.trHist_XUser;
END;
GO
